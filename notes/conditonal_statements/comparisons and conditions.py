a = 4
b = 4.1
print(a == b)  # Is a equal to b?
# == means equal to
print(a != b)  # Is a not equal to b?
# != means not equal to
print(a > b)  # Is a greater than b?

# in
is_in = "a" in "abc"  # True
print(is_in)

fruits = ["apple", "banana", "pineapple"]
fruit = input()
if fruit in fruits:
    print(fruit)
else:
    print("Not found!")

